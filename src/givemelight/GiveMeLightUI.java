    private int getBrightness(){
        try {
            FileReader brightness = new FileReader(Singleton.PREFERED_DIR+Singleton.BRIGHTNESS_STR);            
            BufferedReader br = new BufferedReader(brightness);
            
            return Integer.parseInt(br.readLine());
        } catch (IOException e) {
            jFormattedTextField1.setText("NaN");
        }
        return -1;
    }
    
    private void InitializeComponents(){
        try {
            FileReader brightness = new FileReader(Singleton.DEFAULT_DIR+Singleton.BRIGHTNESS_STR);
            FileReader max_brightness = new FileReader(Singleton.DEFAULT_DIR+Singleton.MAX_BRIGHTNESS_STR);
            
            BufferedReader br = new BufferedReader(brightness);
            
            jFormattedTextField1.setText(br.readLine());
            
            br = new BufferedReader(max_brightness);
            
            Singleton.MAX_BRIGHTNESS = Integer.parseInt(br.readLine());
            jSlider1.setMaximum(Singleton.MAX_BRIGHTNESS);
            jSlider1.setMinimum(1);
            jSlider1.setValue(Integer.parseInt(jFormattedTextField1.getText()));
                        
            jLabel4.setText(Integer.toString(jSlider1.getMinimum()));
            jLabel5.setText(Integer.toString(jSlider1.getMaximum()));
            
        } catch (IOException e) {
            jFormattedTextField1.setText("NaN");
        }
    }
    
