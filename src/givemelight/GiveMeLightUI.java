    private void jSlider1StateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_jSlider1StateChanged
        String VALUE = Integer.toString(jSlider1.getValue());
        jFormattedTextField1.setText(VALUE);
        
        String theCommand = "echo \"" + Singleton.ROOT_PASS + "\"| sudo -S bash -c \"echo '" + VALUE + "' > " + Singleton.PREFERED_DIR + Singleton.BRIGHTNESS_STR + "\"";
        
        try {
            Runtime.getRuntime().exec(new String[] { "bash", "-c", theCommand });            
        } catch (IOException ex) {
            Logger.getLogger(GiveMeLightUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jSlider1StateChanged

    private void jFormattedTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jFormattedTextField1KeyPressed
        
        int VALUE = Integer.parseInt(jFormattedTextField1.getText());
        
        if (VALUE <= 0 )
            jSlider1.setValue(1);
        else if (VALUE > Singleton.MAX_BRIGHTNESS)
            jSlider1.setValue(Singleton.MAX_BRIGHTNESS);
        else
            jSlider1.setValue(VALUE);

    }//GEN-LAST:event_jFormattedTextField1KeyPressed

